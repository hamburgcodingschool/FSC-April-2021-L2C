// Modulo operator % - gives us back the rest of a division;
// Example: console.log(5 % 4) -> will print 1

year = 20;
rest4 = (year % 4);
rest3 = (year % 3);

console.log("Rest of dividing 20 to 4: " + rest4);
console.log("Rest of dividing 20 to 3: " + rest3);

